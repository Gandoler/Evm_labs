#include <stdio.h>

int main() {
    char s1[100]; // предположим, что максимальная длина строки s1 - 99 символов
    char s2[50];  // предположим, что максимальная длина строки s2 - 49 символов
    char s3[100]; // предположим, что максимальная длина строки s3 - 99 символов
    
    // ввод слова без пробелов s1
    printf("Введите слово без пробелов s1: ");
    scanf("%99s", s1); // вводим не более 99 символов
    
    // ввод слова s2 с ограничением на размер буфера
    printf("Введите слово s2 (не более 49 символов): ");
    scanf("%49s", s2); // вводим не более 49 символов
    
    // очистка буфера после второго ввода
    while (getchar() != '\n'); // очистить оставшийся символ новой строки из буфера
    
    // ввод строки s3, возможно, содержащей пробелы
    printf("Введите строку s3: ");
    scanf(" %[^\n]", s3); // вводим всю строку до символа новой строки
    
    // вывод введенных строк
    printf("\n***%s***\n", s1);
    printf("\n***%s***\n", s2);
    printf("\n***%s***\n", s3);

    return 0;
}
