#include <stdio.h>

int main() {
    // Задаем массив 16-битных целых чисел
    unsigned short Ms[5] = {0xFADE, 0xFADE, 0xFADE, 0xFADE, 0xFADE};

    // Вывод массива до ввода
    printf("Массив до ввода:\n");
    for (int i = 0; i < 5; ++i) {
        printf("%04X ", Ms[i]); // Вывод в шестнадцатеричном формате с четырьмя символами
    }
    printf("\n");

    // Ввод новых значений для всех элементов массива
    printf("Введите новые значения для всех элементов массива (через пробел): ");
    int numScanned = scanf("%hx %hx %hx %hx %hx", &Ms[0], &Ms[1], &Ms[2], &Ms[3], &Ms[4]);
    if (numScanned != 5) {
        printf("Ошибка ввода. Введено %d элементов, ожидалось 5.\n", numScanned);
        return 1;
    }

    // Вывод массива после ввода
    printf("Массив после ввода:\n");
    for (int i = 0; i < 5; ++i) {
        printf("%04X ", Ms[i]); // Вывод в шестнадцатеричном формате с четырьмя символами
    }
    printf("\n");

    return 0;
}
